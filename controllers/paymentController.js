const pathManager = require('../config/pathManager');
const mpesaService = require('../services/mpesaService');

// ============ STK Push (Auto‑generated payment) ============
exports.initiateSTKPush = async (req, res) => {
  try {
    const { phoneNumber, itemId, itemType } = req.body;
    const userId = req.user.id;

    const phoneRegex = /^(?:\+254|0)[17]\d{8}$/;
    if (!phoneNumber || !phoneRegex.test(phoneNumber)) {
      return res.status(400).json({ message: 'Valid Kenyan phone number required (e.g., 0712345678)' });
    }

    const Model = itemType === 'document' ? Document : Software;
    const item = await Model.findById(itemId);
    if (!item) return res.status(404).json({ message: 'Item not found' });

    const existingOrder = await Order.findOne({ user: userId, 'items.itemId': itemId, status: 'completed' });
    if (existingOrder) return res.status(400).json({ message: 'You already own this item' });

    const existingPending = await Transaction.findOne({
      user: userId,
      itemId,
      status: { $in: ['pending', 'processing'] },
      paymentMethod: 'autogenerated'
    });
    if (existingPending) {
      return res.status(400).json({
        message: 'You already have a pending payment. Please wait or check status.',
        transactionId: existingPending._id
      });
    }

    const transaction = await Transaction.create({
      user: userId,
      itemId: item._id,
      itemType,
      itemTitle: item.title,
      amount: item.price,
      paymentMethod: 'autogenerated',
      status: 'pending',
      phoneNumber,
      metadata: {
        initiatedAt: new Date().toISOString(),
        itemTitle: item.title,
        itemPrice: item.price,
        ipAddress: req.ip,
        userAgent: req.headers['user-agent']
      }
    });

    const accountRef = `${itemType === 'document' ? 'DOC' : 'SW'}-${item._id.toString().slice(-6)}`;
    const mpesaResponse = await mpesaService.initiateSTKPush(
      phoneNumber,
      item.price,
      accountRef,
      `Payment for ${item.title}`
    );

    transaction.metadata.mpesaResponse = mpesaResponse;
    transaction.metadata.checkoutRequestId = mpesaResponse.CheckoutRequestID;
    transaction.status = 'processing';
    await transaction.save();

    console.log(`✅ STK Push initiated for transaction: ${transaction._id}`);
    res.json({
      success: true,
      message: 'STK Push initiated. Check your phone to complete payment.',
      transactionId: transaction._id,
      checkoutRequestId: mpesaResponse.CheckoutRequestID,
      status: 'processing'
    });
  } catch (error) {
    console.error('STK Push error:', error);
    res.status(500).json({
      success: false,
      message: 'Failed to initiate payment. Please try again or use manual payment.'
    });
  }
};

// ============ Manual payment initiation ============
exports.initiateManualPayment = async (req, res) => {
  try {
    const { itemId, itemType } = req.body;
    const userId = req.user.id;

    const Model = itemType === 'document' ? Document : Software;
    const item = await Model.findById(itemId);
    if (!item) return res.status(404).json({ message: 'Item not found' });

    const existingOrder = await Order.findOne({ user: userId, 'items.itemId': itemId, status: 'completed' });
    if (existingOrder) return res.status(400).json({ message: 'You already own this item' });

    const existingPending = await Transaction.findOne({
      user: userId,
      itemId,
      status: 'pending',
      paymentMethod: 'manual'
    });
    if (existingPending) {
      return res.json({
        success: true,
        message: 'You already have a pending payment for this item',
        transactionId: existingPending._id,
        existing: true
      });
    }

    const transaction = await Transaction.create({
      user: userId,
      itemId: item._id,
      itemType,
      itemTitle: item.title,
      amount: item.price,
      paymentMethod: 'manual',
      status: 'pending',
      metadata: {
        initiatedAt: new Date().toISOString(),
        ipAddress: req.ip,
        userAgent: req.headers['user-agent']
      }
    });

    console.log(`✅ Manual payment initiated: ${transaction._id}`);
    res.status(201).json({
      success: true,
      message: 'Manual payment initiated. Please send money and upload screenshot or paste confirmation.',
      transactionId: transaction._id,
      amount: item.price,
      businessNumber: process.env.BUSINESS_PHONE || '0768784909',
      businessName: process.env.BUSINESS_NAME || 'DocuSoft Store'
    });
  } catch (error) {
    console.error('Manual payment initiation error:', error);
    res.status(500).json({ success: false, message: 'Failed to initiate manual payment' });
  }
};

// ============ Upload screenshot (local file) ============
exports.uploadScreenshot = async (req, res) => {
  try {
    const { transactionId } = req.params;
    const userId = req.user.id;

    if (!req.file) {
      return res.status(400).json({ message: 'No screenshot uploaded' });
    }

    const allowedTypes = ['image/jpeg', 'image/jpg', 'image/png', 'image/gif', 'image/webp'];
    if (!allowedTypes.includes(req.file.mimetype)) {
      return res.status(400).json({ message: 'Invalid file type. Please upload JPG, PNG, GIF, or WEBP image.' });
    }

    if (req.file.size > 10 * 1024 * 1024) {
      return res.status(413).json({ message: 'Screenshot too large. Max 10MB.' });
    }

    const transaction = await Transaction.findById(transactionId);
    if (!transaction) return res.status(404).json({ message: 'Transaction not found' });
    if (transaction.user.toString() !== userId) return res.status(403).json({ message: 'Not authorized' });
    if (transaction.status !== 'pending') return res.status(400).json({ message: `Payment already ${transaction.status}` });

    const relativePath = `screenshots/${req.file.filename}`;
    transaction.screenshotUrl = pathManager.getPublicUrl(relativePath);
    transaction.screenshotPath = req.file.path;
    
    if (!transaction.metadata) transaction.metadata = {};
    transaction.metadata.uploadedAt = new Date().toISOString();
    transaction.metadata.uploadType = 'screenshot';
    
    await transaction.save();

    console.log(`✅ Screenshot saved for transaction: ${transactionId}`);
    console.log(`   Screenshot URL: ${transaction.screenshotUrl}`);
    
    res.json({
      success: true,
      message: 'Screenshot uploaded. Awaiting admin verification.',
      transactionId: transaction._id
    });
  } catch (error) {
    console.error('Screenshot upload error:', error);
    res.status(500).json({ message: 'Failed to upload screenshot' });
  }
};


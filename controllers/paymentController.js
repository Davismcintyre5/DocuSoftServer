// ============ Check payment status ============
let statusCache = new Map();

exports.checkPaymentStatus = async (req, res) => {
  try {
    const { itemId } = req.params;
    const userId = req.user.id;

    const cacheKey = `${userId}-${itemId}`;
    const cached = statusCache.get(cacheKey);
    if (cached && Date.now() - cached.timestamp < 5000) {
      return res.json(cached.data);
    }

    const order = await Order.findOne({ user: userId, 'items.itemId': itemId, status: 'completed' });
    if (order) {
      const response = {
        purchased: true,
        status: 'completed',
        orderId: order._id,
        message: 'You own this item'
      };
      statusCache.set(cacheKey, { data: response, timestamp: Date.now() });
      return res.json(response);
    }

    const transaction = await Transaction.findOne({ user: userId, itemId }).sort({ createdAt: -1 });
    if (transaction) {
      let statusMessage = '';
      switch (transaction.status) {
        case 'pending': statusMessage = 'Payment pending verification. Please wait.'; break;
        case 'processing': statusMessage = 'Payment processing. Check your phone for M-Pesa prompt.'; break;
        case 'approved': statusMessage = 'Payment approved, order being processed.'; break;
        case 'completed': statusMessage = 'Payment completed. You can download now.'; break;
        case 'rejected': statusMessage = `Payment rejected: ${transaction.rejectionReason || 'Please contact support.'}`; break;
        case 'failed': statusMessage = 'Payment failed. Please try again.'; break;
        default: statusMessage = 'Payment status unknown. Contact support.';
      }
      const response = {
        purchased: false,
        transactionId: transaction._id,
        status: transaction.status,
        message: statusMessage,
        createdAt: transaction.createdAt
      };
      statusCache.set(cacheKey, { data: response, timestamp: Date.now() });
      return res.json(response);
    }

    const response = { purchased: false, status: 'not_paid', message: 'No payment found for this item' };
    statusCache.set(cacheKey, { data: response, timestamp: Date.now() });
    res.json(response);
  } catch (error) {
    console.error('Check payment status error:', error);
    res.status(500).json({ message: 'Failed to check status' });
  }
};


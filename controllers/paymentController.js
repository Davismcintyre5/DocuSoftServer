// ============ M-Pesa callback ============
exports.mpesaCallback = async (req, res) => {
  try {
    const data = req.body;
    console.log('📱 M-Pesa callback received:', JSON.stringify(data, null, 2));

    const { CheckoutRequestID, ResultCode, ResultDesc } = data.Body?.stkCallback || {};
    if (!CheckoutRequestID) {
      return res.status(200).json({ ResultCode: 1, ResultDesc: 'Invalid callback data' });
    }

    const transaction = await Transaction.findOne({ 'metadata.checkoutRequestId': CheckoutRequestID });
    if (!transaction) {
      return res.status(200).json({ ResultCode: 1, ResultDesc: 'Transaction not found' });
    }

    if (transaction.status === 'completed') {
      return res.status(200).json({ ResultCode: 0, ResultDesc: 'Already processed' });
    }

    if (ResultCode === 0) {
      transaction.status = 'completed';
      transaction.metadata.callbackData = data;
      transaction.metadata.mpesaCode = data.Body?.stkCallback?.MerchantRequestID;
      transaction.metadata.completedAt = new Date().toISOString();
      await transaction.save();

      const Model = transaction.itemType === 'document' ? Document : Software;
      const item = await Model.findById(transaction.itemId);
      if (item) {
        const order = await Order.create({
          user: transaction.user,
          items: [{
            itemId: transaction.itemId,
            itemType: transaction.itemType,
            title: item.title,
            price: transaction.amount,
            downloadCount: 0
          }],
          totalAmount: transaction.amount,
          paymentMethod: transaction.paymentMethod,
          transactionId: transaction._id,
          status: 'completed',
          completedAt: new Date()
        });
        transaction.orderCreated = true;
        transaction.orderId = order._id;
        await transaction.save();
        console.log(`✅ Order created: ${order._id}`);
      }
    } else {
      transaction.status = 'failed';
      transaction.metadata.callbackData = data;
      transaction.metadata.failureReason = ResultDesc;
      await transaction.save();
    }

    res.status(200).json({ ResultCode: 0, ResultDesc: 'Success' });
  } catch (error) {
    console.error('M-Pesa callback error:', error);
    res.status(200).json({ ResultCode: 1, ResultDesc: 'Error processing callback' });
  }
};

// ============ Helper to create order from transaction (admin approval) ============
const createOrderFromTransaction = async (transactionId) => {
  const transaction = await Transaction.findById(transactionId);
  if (!transaction || transaction.orderCreated) return;
  const Model = transaction.itemType === 'document' ? Document : Software;
  const item = await Model.findById(transaction.itemId);
  if (!item) return;
  const order = await Order.create({
    user: transaction.user,
    items: [{
      itemId: transaction.itemId,
      itemType: transaction.itemType,
      title: item.title,
      price: transaction.amount,
      downloadCount: 0
    }],
    totalAmount: transaction.amount,
    paymentMethod: transaction.paymentMethod,
    transactionId: transaction._id,
    status: 'completed',
    completedAt: new Date()
  });
  transaction.status = 'completed';
  transaction.orderCreated = true;
  transaction.orderId = order._id;
  await transaction.save();
  return order;
};

exports.createOrderFromTransaction = createOrderFromTransaction;